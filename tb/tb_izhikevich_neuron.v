`timescale 1ns/1ps

module tb_izhikevich_neuron;

    reg signed [17:0] a, b, c, d, I;
    reg clk, rst_n;

    wire signed [17:0] v, u;
    wire spike;

    izhikevich_neuron dut (
        .clk(clk),
        .rst_n(rst_n),
        .I(I),
        .a(a),
        .b(b),
        .c(c),
        .d(d),
        .v(v),
        .u(u),
        .spike(spike)
    );

    always #5 clk = ~clk;

    initial begin
        clk = 0;

        //-----------------------------
        // Regular Spiking
        //-----------------------------
        a = 18'sh051E;  // 0.02
        b = 18'sh3333;  // 0.2
        c = 18'sh3599A; // -0.65
        d = 18'sh147B;  // 0.08
        I = 18'sh2666;  // 0.15

        rst_n = 0;
        #20 rst_n = 1;

        #40000;
        I = 0;
        #40000;

        //-----------------------------
        // Intrinsically Bursting
        //-----------------------------
        a = 18'sh051E; //0.02
        b = 18'sh3333; //0.2
        c = 18'sh37333;//-0.55
        d = 18'sh0A3D; //0.04
        I = 18'sh2666;

        rst_n = 0;
        #20 rst_n = 1;

        #40000;
        I = 0;
        #40000;

        //-----------------------------
        // Chattering
        //-----------------------------
        a = 18'sh051E; //0.02
        b = 18'sh3333; //0.2
        c = 18'sh38000;//-0.5
        d = 18'sh051E; //0.02
        I = 18'sh2666;

        rst_n = 0;
        #20 rst_n = 1;

        #40000;
        I = 0;
        #40000;

        //-----------------------------
        // Fast Spiking
        //-----------------------------
        a = 18'sh199A; //0.1
        b = 18'sh3333; //0.2
        c = 18'sh3599A;//-0.65
        d = 18'sh051E; //0.02
        I = 18'sh2666;

        rst_n = 0;
        #20 rst_n = 1;

        #40000;
        I = 0;
        #40000;

        //-----------------------------
        // Thalamo-cortical
        //-----------------------------
        a = 18'sh051E; //0.02
        b = 18'sh4000; //0.25
        c = 18'sh32148;//-0.65
        d = 18'sh0021; //0.0005
        I = 18'sh2666;

        rst_n = 0;
        #20 rst_n = 1;

        #40000;
        I = 0;
        #40000;

        //-----------------------------
        // Resonator
        //-----------------------------
        a = 18'sh199A; //0.1
        b = 18'sh428F; //0.26
        c = 18'sh3599A;//-0.65
        d = 18'sh051E; //0.02
        I = 18'sh2666;

        rst_n = 0;
        #20 rst_n = 1;

        #40000;
        I = 0;
        #40000;

        //-----------------------------
        // Low Threshold Spiking
        //-----------------------------
        a = 18'sh051E; //0.02
        b = 18'sh4000; //0.25
        c = 18'sh3599A;//-0.65
        d = 18'sh051E; //0.02
        I = 18'sh2666;

        rst_n = 0;
        #20 rst_n = 1;

        #40000;
        I = 0;
        #40000;

        $finish;
    end

endmodule
